def columns(ch):
    """7 column bytes: glyph centered (cols 1-5 of 7), bit 0 = top row."""
    rows = G[ch]
    cols = [0] * 7
    for c in range(W):
        v = 0
        for r in range(H):
            if rows[r][c] == '#':
                v |= 1 << r
        cols[c + 1] = v
    return cols


def proof():
    chars = [chr(c) for c in range(0x20, 0x7F)]
    for base in range(0, len(chars), 8):
        group = chars[base:base + 8]
        print("   " + "  ".join(f"[{c}] 0x{ord(c):02X}" for c in group))
        for r in range(H):
            line = []
            for c in group:
                cols = columns(c)
                line.append("".join('#' if (cols[i] >> r) & 1 else '.' for i in range(7)))
            print("   " + "   ".join(line))
        print()


def header():
    print("static constexpr Byte  s_kDraftGlyphs[95][7] =")
    print("{")
    for code in range(0x20, 0x7F):
        ch = chr(code)
        cols = columns(ch)
        body = ", ".join(f"0x{v:02X}" for v in cols)
        name = "space" if ch == ' ' else ("backslash" if ch == '\\' else ch)
        print(f"    {{ {body} }},   // 0x{code:02X} '{name}'")
    print("};")


if __name__ == "__main__":
    (proof if (len(sys.argv) < 2 or sys.argv[1] == "proof") else header)()
