# Casso draft-font generator (US6 T046). Original glyph designs, authored as
# ASCII art on a 5-wide x 8-tall grid (rows 0-6 = main body, row 7 = descender)
# and emitted as 7-column bytes (glyph centered in a 7-column cell, cols 0 and
# 6 blank) with bit 0 = TOP row, matching ESC G's pin convention.
#
# Usage: python gen_font.py proof   -> ASCII proof sheet for eyeballing
#        python gen_font.py header  -> the DraftFont.h table body

